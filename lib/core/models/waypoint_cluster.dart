/// Types of waypoint groups
enum WaypointGroupType {
  /// Custom user-created group
  custom,

  /// Favorites group
  favorites,

  /// Recently visited group
  recent,

  /// Planned waypoints group
  planned,

  /// Completed waypoints group
  completed,

  /// Shared waypoints group
  shared,

  /// Imported waypoints group
  imported,

  /// Route waypoints group
  route,

  /// Emergency waypoints group
  emergency,
}

/// Extension for waypoint group types
extension WaypointGroupTypeExtension on WaypointGroupType {
  /// Display name for the group type
  String get displayName {
    switch (this) {
      case WaypointGroupType.custom:
        return 'Custom';
      case WaypointGroupType.favorites:
        return 'Favorites';
      case WaypointGroupType.recent:
        return 'Recent';
      case WaypointGroupType.planned:
        return 'Planned';
      case WaypointGroupType.completed:
        return 'Completed';
      case WaypointGroupType.shared:
        return 'Shared';
      case WaypointGroupType.imported:
        return 'Imported';
      case WaypointGroupType.route:
        return 'Route';
      case WaypointGroupType.emergency:
        return 'Emergency';
    }
  }

  /// Whether this group type is system-managed
  bool get isSystemGroup {
    switch (this) {
      case WaypointGroupType.recent:
      case WaypointGroupType.completed:
      case WaypointGroupType.imported:
        return true;
      case WaypointGroupType.custom:
      case WaypointGroupType.favorites:
      case WaypointGroupType.planned:
      case WaypointGroupType.shared:
      case WaypointGroupType.route:
      case WaypointGroupType.emergency:
