        'is_active': isActive ? 1 : 0,
        'sort_order': sortOrder,
        'parent_group_id': parentGroupId,
        'group_type': groupType.name,
      };

  /// Create a copy with updated values
  WaypointGroup copyWith({
    String? id,
    String? name,
    List<String>? waypointIds,
    DateTime? createdAt,
    String? userId,
    String? description,
    Color? color,
    IconData? iconData,
    List<String>? tags,
    bool? isActive,
    int? sortOrder,
    String? parentGroupId,
    WaypointGroupType? groupType,
