  }

  /// Default icon for this group type
  IconData get defaultIcon {
    switch (this) {
      case WaypointGroupType.custom:
        return Icons.folder;
      case WaypointGroupType.favorites:
        return Icons.favorite;
      case WaypointGroupType.recent:
        return Icons.history;
      case WaypointGroupType.planned:
        return Icons.schedule;
      case WaypointGroupType.completed:
        return Icons.check_circle;
      case WaypointGroupType.shared:
        return Icons.share;
      case WaypointGroupType.imported:
        return Icons.download;
      case WaypointGroupType.route:
        return Icons.route;
      case WaypointGroupType.emergency:
        return Icons.emergency;
    }
  }
}
